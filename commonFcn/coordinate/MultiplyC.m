%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
% Input
% C12: [3*3*N]
% C23: [3*3*N]
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
% Output
% C13: [3*3*N]

function C13 = MultiplyC( C12,C23 )
Nframes = size( C12,3 );
C13 = zeros(3,3,Nframes);
for k=1:Nframes
    C13(k,:,:) = C23(:,:,k)*C12(:,:,k) ;
end